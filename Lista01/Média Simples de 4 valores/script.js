let valor1 = Number(prompt("Digite o primeiro valor: "))
let valor2 = Number(prompt("Digite o segundo valor: "))
let valor3 = Number(prompt("Digite o terceiro valor: "))
let valor4 = Number(prompt("Digite o quarto valor: "))


let media = (valor1 + valor2 + valor3 + valor4) / 4

alert(`A média simples entre os valores ${valor1}, ${valor2}, ${valor3} e ${valor4} é igual a ${media}`)


